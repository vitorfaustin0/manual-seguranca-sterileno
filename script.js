// Fun√ß√£o para verificar login
function checkLogin() {
    const usernameInput = document.getElementById('username-input');
    const passwordInput = document.getElementById('password-input');
    const errorMessage = document.getElementById('error-message');
    const loginScreen = document.getElementById('login-screen');
    const mainContent = document.getElementById('main-content');
    
    const username = usernameInput.value.toLowerCase().trim();
    const password = passwordInput.value;
    
    if (username === LOGIN_CREDENTIALS.username && password === LOGIN_CREDENTIALS.password) {
        // Login correto - mostrar conte√∫do
        loginScreen.style.display = 'none';
        mainContent.style.display = 'block';
    } else {
        // Login incorreto - mostrar erro
        errorMessage.style.display = 'flex';
        errorMessage.innerHTML = '<i class="fas fa-exclamation-triangle"></i>Usu√°rio ou senha incorretos! Tente novamente.';
        usernameInput.value = '';
        passwordInput.value = '';
        usernameInput.focus();
        
        // Esconder erro ap√≥s 3 segundos
        setTimeout(() => {
            errorMessage.style.display = 'none';
        }, 3000);
    }
}

// Verificar se j√° est√° autenticado
function checkAuthentication() {
    // Sempre mostrar tela de login (removido localStorage)
    const loginScreen = document.getElementById('login-screen');
    const mainContent = document.getElementById('main-content');
    
    loginScreen.style.display = 'flex';
    mainContent.style.display = 'none';
}

// Permitir login com Enter
function handleKeyPress(event) {
    if (event.key === 'Enter') {
        checkLogin();
    }
}

